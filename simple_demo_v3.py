def load_env_file():
    """Load environment variables from .env file if it exists."""
    env_file = Path(".env")
    if env_file.exists():
        with open(env_file, 'r') as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith('#') and '=' in line:
                    key, value = line.split('=', 1)
                    os.environ[key.strip()] = value.strip()


def create_github_issue(repo_owner, repo_name, github_token, title, description):
    """Create a real GitHub issue using the GitHub API."""
    
    url = f"https://api.github.com/repos/{repo_owner}/{repo_name}/issues"
    
    headers = {
        "Authorization": f"token {github_token}",
        "Accept": "application/vnd.github.v3+json",
        "Content-Type": "application/json"
    }
    
    issue_data = {
        "title": title,
        "body": description,
        "labels": ["enhancement", "copilot-test"]
    }
    
    print(f"ğŸš€ Creating real GitHub issue...")
    print(f"ğŸ“ Repository: {repo_owner}/{repo_name}")
    print(f"ğŸ“ Title: {title}")
    
    try:
        response = requests.post(url, headers=headers, json=issue_data)
        
        if response.status_code == 201:
            issue = response.json()
            print(f"âœ… SUCCESS! Issue #{issue['number']} created!")
            print(f"ğŸ”— URL: {issue['html_url']}")
            return issue
        else:
            print(f"âŒ Failed to create issue: {response.status_code}")
            print(f"Response: {response.text}")
            return None
            
    except Exception as e:
        print(f"âŒ Error creating issue: {e}")
        return None


